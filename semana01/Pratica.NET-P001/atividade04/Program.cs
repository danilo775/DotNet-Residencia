// Declara as variáveis x e y
int x = 10;
int y = 3;

// Calcula o resultado da adição
int soma = x + y;

// Calcula o resultado da subtração
int subtracao = x - y;

// Calcula o resultado da multiplicação
int multiplicacao = x * y;

// Calcula o resultado da divisão
int divisao = x / y;

// Exibe os resultados
Console.WriteLine("A soma é: {0}", soma);
Console.WriteLine("A subtração é: {0}", subtracao);
Console.WriteLine("A multiplicação é: {0}", multiplicacao);
Console.WriteLine("A divisão é: {0}", divisao);
