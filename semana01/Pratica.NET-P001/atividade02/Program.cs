// O .NET oferece suporte a cinco tipos de dados numéricos inteiros:
// SByte - um byte assinado com um intervalo de -128 a 127.
// Byte - um byte não assinado com um intervalo de 0 a 255.
// Short - um short assinado com um intervalo de -32.768 a 32.767.
// Int - um int assinado com um intervalo de -2.147.483.648 a 2.147.483.647.
// Long - um long assinado com um intervalo de -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807.


// Declara uma variável do tipo SByte
SByte idade = -12;
Console.WriteLine(idade); // Imprime -12


// Declara uma variável do tipo Byte
Byte cor = 0xFF;
Console.WriteLine(cor); // Imprime 255

// Declara uma variável do tipo Short
Short quantidade = -32768;
Console.WriteLine(quantidade); // Imprime -32768

// Declara uma variável do tipo Int
Int idade = 21;
Console.WriteLine(idade); // Imprime 21

// Declara uma variável do tipo Long
Long quantidade = 9223372036854775807;
Console.WriteLine(quantidade); // Imprime 9223372036854775807




